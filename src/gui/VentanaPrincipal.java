    private void listar() {

        Vector columnas = new Vector();
        columnas.add("Nombre");
        columnas.add("Cedula");
        columnas.add("Numero Celular");
        columnas.add("Registro Medico");
        columnas.add("Especialidad");
        DefaultTableModel dtm = new DefaultTableModel(null, columnas);
        ArrayList<Profesional> usuarios = controlador.obtenerProfesionals();
        for (Profesional usu : usuarios) {
            Vector datos = new Vector();
            datos.add(usu.getNombre());
            datos.add(usu.getCedula());
            datos.add(usu.getNumCel());
            datos.add(usu.getRm());
            datos.add(usu.getEspecializacion());
            dtm.addRow(datos);
        }
        MiPrimeraTabla.setModel(dtm);
    }

    private void listarMedicamentos() {

        Vector columnas = new Vector();
        columnas.add("Nombre");
        columnas.add("Cantidad");

        DefaultTableModel dtm = new DefaultTableModel(null, columnas);
        ArrayList<Medicamento> medicamento = controladorM.obtenerMedicamento();
        for (Medicamento me : medicamento) {
            Vector datos = new Vector();
            datos.add(me.getNombre());
            datos.add(me.getCantidad());

            dtm.addRow(datos);
        }
        MiSegundaTabla.setModel(dtm);
    }

    public void noRecibirLetras(KeyEvent e) {
        if (!Character.isDigit((char) e.getKeyChar())) {
            // esta instruccion borra el caracter digitado
            //cuando no es un numero
            e.setKeyChar((char) KeyEvent.VK_CLEAR);
        }
    }

    public void noRecibirNumeros(KeyEvent e) {
        /*
         * hay que evaluar si el caracter dIGitado no es una letra
         * es decir si es una letra o un caracter especial
         */

        if (!Character.isLetter((char) e.getKeyChar())) {
            // esta instruccion borra el caracter digitado
            //cuando no es una letra
            e.setKeyChar((char) KeyEvent.VK_CLEAR);
        }

    }


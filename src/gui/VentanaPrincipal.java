    private void jBBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBBuscarActionPerformed

        String medico = JOptionPane.showInputDialog(null, "Ingrese la cedula del profesional:");

        Profesional ced = controlador.buscarProfesional(medico);
        if (ced != null) {

            JOptionPane.showMessageDialog(null, "Se ha encontrado el profesional.");
            jTFNombreMedico.setText(ced.getNombre());
            jTFCedulaMedico.setText(ced.getCedula());
            jTFRegisMedico.setText(ced.getRm());
            jCBEspeciMedico.setSelectedItem(ced.getEspecializacion());
            jTFNumTelMedico.setText(ced.getNumCel());

        } else {
            JOptionPane.showMessageDialog(null, "No se ha encontrado el profesional ingresado");
            LimpiarCampos();
        }
    }//GEN-LAST:event_jBBuscarActionPerformed

    private void jBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarActionPerformed

        String nom = jTFNombreMedico.getText();
        String ced = jTFCedulaMedico.getText();
        String numCel = jTFNumTelMedico.getText();
        String rm = jTFRegisMedico.getText();
        String especializacion = (String) jCBEspeciMedico.getSelectedItem();
        if (nom.isEmpty() || ced.isEmpty() || numCel.isEmpty() || rm.isEmpty() || especializacion.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe llenar todos los campos");

        } else {
            Profesional p = new Profesional(nom, ced, numCel, rm, especializacion);
            boolean ban = controlador.RegistrarProfesional(p);
            if (!ban) {
                JOptionPane.showMessageDialog(null, "Se ha registrado correctamente");
            } else {
                JOptionPane.showMessageDialog(null, "El profesional no se registro");
            }

            LimpiarCampos();

            listar();
        }
    }//GEN-LAST:event_jBGuardarActionPerformed


